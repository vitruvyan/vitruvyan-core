"""
E2E Test: Qdrant Vector Operations
====================================
Verifies upsert, search, and delete operations on Qdrant using
real embeddings generated by the Embedding API.

Requires: Qdrant (localhost:9333), Embedding API (localhost:9010).
"""
import uuid

import httpx
import pytest

pytestmark = [pytest.mark.e2e]

E2E_COLLECTION = "e2e_test_vectors"


@pytest.fixture(scope="module")
def qdrant_collection(qdrant_http):
    """Create a test collection and clean up after."""
    # Create collection with 768 dimensions (nomic-embed-text-v1.5)
    r = httpx.put(
        f"{qdrant_http}/collections/{E2E_COLLECTION}",
        json={
            "vectors": {"size": 768, "distance": "Cosine"},
        },
        timeout=10.0,
    )
    assert r.status_code in (200, 409), f"Failed to create collection: {r.text}"

    yield E2E_COLLECTION

    # Cleanup
    httpx.delete(f"{qdrant_http}/collections/{E2E_COLLECTION}", timeout=10.0)


def _get_embedding(embedding_api: str, text: str) -> list:
    """Get embedding vector from the Embedding API."""
    r = httpx.post(
        f"{embedding_api}/v1/embeddings/create",
        json={"text": text, "source": "e2e_test"},
        timeout=15.0,
    )
    assert r.status_code == 200, f"Embedding API error: {r.text[:200]}"
    data = r.json()
    assert data.get("success"), f"Embedding not successful: {data}"
    return data["embedding"]


