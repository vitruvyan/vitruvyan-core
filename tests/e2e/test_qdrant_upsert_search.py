class TestEmbeddingGeneration:
    """Embedding API must produce valid vectors."""

    def test_single_embedding(self, embedding_api):
        """Generate a single embedding and validate dimensions."""
        vec = _get_embedding(embedding_api, "test di embedding vettoriale")
        assert isinstance(vec, list)
        assert len(vec) == 768
        assert all(isinstance(v, float) for v in vec)

    def test_embedding_deterministic(self, embedding_api):
        """Same text must produce identical embeddings."""
        text = "deterministic embedding test"
        vec1 = _get_embedding(embedding_api, text)
        vec2 = _get_embedding(embedding_api, text)
        assert vec1 == vec2

    def test_different_texts_different_vectors(self, embedding_api):
        """Different texts must produce different embeddings."""
        vec1 = _get_embedding(embedding_api, "il sole splende forte")
        vec2 = _get_embedding(embedding_api, "la pioggia cade lenta")
        assert vec1 != vec2

    def test_batch_embeddings(self, embedding_api):
        """Batch embedding endpoint must work."""
        r = httpx.post(
            f"{embedding_api}/v1/embeddings/batch",
            json={"texts": ["testo uno", "testo due", "testo tre"], "source": "e2e"},
            timeout=15.0,
        )
        assert r.status_code == 200
        data = r.json()
        assert data.get("success")
        embeddings = data.get("embeddings", [])
        assert len(embeddings) == 3
        assert all(len(e) == 768 for e in embeddings)


class TestUpsertAndSearch:
    """Upsert vectors and search for similarity."""

    def test_upsert_single_point(self, qdrant_http, embedding_api, qdrant_collection):
        """Upsert a single vector point."""
        vec = _get_embedding(embedding_api, "intelligenza artificiale generativa")
        point_id = str(uuid.uuid4())

        r = httpx.put(
            f"{qdrant_http}/collections/{qdrant_collection}/points",
            json={
                "points": [{
                    "id": point_id,
                    "vector": vec,
                    "payload": {"text": "intelligenza artificiale generativa", "source": "e2e"},
                }]
            },
            timeout=10.0,
        )
        assert r.status_code == 200, f"Upsert failed: {r.text}"

    def test_upsert_multiple_points(self, qdrant_http, embedding_api, qdrant_collection):
        """Upsert 3 vectors with different content."""
        texts = [
            "machine learning supervisionato",
            "reti neurali profonde",
            "elaborazione del linguaggio naturale",
        ]
        points = []
        for text in texts:
            vec = _get_embedding(embedding_api, text)
            points.append({
                "id": str(uuid.uuid4()),
                "vector": vec,
                "payload": {"text": text, "source": "e2e"},
            })

        r = httpx.put(
            f"{qdrant_http}/collections/{qdrant_collection}/points",
            json={"points": points},
            timeout=15.0,
        )
        assert r.status_code == 200

    def test_search_returns_similar_vectors(self, qdrant_http, embedding_api, qdrant_collection):
        """Search must return relevant results ordered by similarity."""
        # First upsert some known content
        texts = [
            "analisi dei dati con python",
            "programmazione funzionale",
            "architettura dei microservizi",
        ]
        for text in texts:
            vec = _get_embedding(embedding_api, text)
            httpx.put(
                f"{qdrant_http}/collections/{qdrant_collection}/points",
                json={"points": [{"id": str(uuid.uuid4()), "vector": vec, "payload": {"text": text}}]},
                timeout=10.0,
            )

        # Wait for indexing
        import time
        time.sleep(1)

        # Search for something similar to "analisi dei dati"
        query_vec = _get_embedding(embedding_api, "analisi dei dati")
        r = httpx.post(
            f"{qdrant_http}/collections/{qdrant_collection}/points/search",
            json={"vector": query_vec, "top": 3, "with_payload": True},
            timeout=10.0,
        )
        assert r.status_code == 200
        results = r.json().get("result", [])
        assert len(results) > 0, "Search returned no results"

        # Results should be ordered by score (descending)
        scores = [r["score"] for r in results]
        assert scores == sorted(scores, reverse=True), "Results not ordered by score"

    def test_search_payload_returned(self, qdrant_http, embedding_api, qdrant_collection):
        """Search results must include payload when requested."""
        # Upsert with rich payload
        vec = _get_embedding(embedding_api, "test payload completo")
        point_id = str(uuid.uuid4())
        httpx.put(
            f"{qdrant_http}/collections/{qdrant_collection}/points",
            json={"points": [{
                "id": point_id,
                "vector": vec,
                "payload": {"text": "test payload completo", "source": "e2e", "category": "test"},
            }]},
            timeout=10.0,
        )

        import time
        time.sleep(1)

        # Search and verify payload
        r = httpx.post(
            f"{qdrant_http}/collections/{qdrant_collection}/points/search",
            json={"vector": vec, "top": 1, "with_payload": True},
            timeout=10.0,
        )
        assert r.status_code == 200
        results = r.json().get("result", [])
        assert len(results) >= 1
        # Top result should have our payload
        payload = results[0].get("payload", {})
        assert payload.get("source") == "e2e"


