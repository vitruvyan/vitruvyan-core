class TestDeleteOperations:
    """Verify point deletion works."""

    def test_delete_by_id(self, qdrant_http, embedding_api, qdrant_collection):
        """Delete a point by ID and verify it's gone."""
        # Upsert a point
        vec = _get_embedding(embedding_api, "punto da cancellare")
        point_id = str(uuid.uuid4())
        httpx.put(
            f"{qdrant_http}/collections/{qdrant_collection}/points",
            json={"points": [{"id": point_id, "vector": vec, "payload": {"text": "da cancellare"}}]},
            timeout=10.0,
        )

        import time
        time.sleep(1)

        # Get count before
        r = httpx.get(f"{qdrant_http}/collections/{qdrant_collection}", timeout=5.0)
        before = r.json()["result"]["points_count"]

        # Delete
        r = httpx.post(
            f"{qdrant_http}/collections/{qdrant_collection}/points/delete",
            json={"points": [point_id]},
            timeout=10.0,
        )
        assert r.status_code == 200

        time.sleep(1)

        # Count should decrease
        r = httpx.get(f"{qdrant_http}/collections/{qdrant_collection}", timeout=5.0)
        after = r.json()["result"]["points_count"]
        assert after < before, f"Point not deleted: before={before}, after={after}"
