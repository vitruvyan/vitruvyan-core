def analyze_all_portfolios(postgres_agent: PostgresAgent) -> Dict[str, List[GuardianInsight]]:
    """
    Analyze all active portfolios and generate insights
    
    Intended for cron job execution (hourly)
    
    Args:
        postgres_agent: PostgreSQL connection
    
    Returns:
        Dict mapping user_id → List[GuardianInsight]
    """
    logger.info("🛡️ Guardian: Analyzing all portfolios...")
    
    guardian = PortfolioGuardianAgent(postgres_agent=postgres_agent)
    
    # Get all unique users with portfolios
    with postgres_agent.connection.cursor() as cur:
        cur.execute("""
            SELECT DISTINCT user_id
            FROM shadow_portfolio_snapshots
            WHERE created_at > NOW() - INTERVAL '7 days'
        """)
        users = [row[0] for row in cur.fetchall()]
    
    results = {}
    for user_id in users:
        insights = guardian.analyze_portfolio(user_id=user_id)
        results[user_id] = insights
    
    logger.info(f"✅ Guardian analysis complete - {len(users)} users, {sum(len(v) for v in results.values())} total insights")
    return results
