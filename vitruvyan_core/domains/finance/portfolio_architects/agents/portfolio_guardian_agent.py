    # ========================= Type 5: Performance Attribution =========================
    
    def _analyze_performance(self, snapshot: Dict[str, Any]) -> List[GuardianInsight]:
        """Analyze winners/losers in portfolio"""
        insights = []
        holdings = snapshot.get("holdings", [])
        
        if not holdings:
            return insights
        
        # Sort holdings by composite_z (proxy for performance)
        sorted_holdings = sorted(
            holdings,
            key=lambda h: h.get("composite_z", 0.0),
            reverse=True
        )
        
        # Top performers
        top_3 = sorted_holdings[:3]
        bottom_3 = sorted_holdings[-3:] if len(sorted_holdings) >= 3 else []
        
        insights.append(GuardianInsight(
            user_id=snapshot["user_id"],
            snapshot_id=snapshot["snapshot_id"],
            insight_type=InsightType.PERFORMANCE_ATTRIBUTION.value,
            severity=InsightSeverity.INFO.value,
            title="Portfolio Performance Attribution",
            description=f"Top performers: {', '.join([h.get('ticker') for h in top_3])}",
            metrics={
                "top_performers": [
                    {
                        "ticker": h.get("ticker"),
                        "weight": h.get("weight"),
                        "composite_z": h.get("composite_z"),
                        "risk_score": h.get("risk_score")
                    }
                    for h in top_3
                ],
                "bottom_performers": [
                    {
                        "ticker": h.get("ticker"),
                        "weight": h.get("weight"),
                        "composite_z": h.get("composite_z"),
                        "risk_score": h.get("risk_score")
                    }
                    for h in bottom_3
                ]
            },
            recommendations=[
                "Monitor top performers for potential profit-taking",
                "Review underperformers for replacement candidates",
                "Consider rebalancing if performance divergence is significant"
            ]
        ))
        
        return insights
    
    # ========================= Database Persistence =========================
    
    def _save_insight(self, insight: GuardianInsight) -> int:
        """
        Save insight to guardian_insights table
        
        Returns:
            insight_id (primary key)
        """
        with self.postgres.connection.cursor() as cur:
            cur.execute("""
                INSERT INTO guardian_insights (
                    user_id,
                    snapshot_id,
                    insight_type,
                    severity,
                    title,
                    description,
                    metrics,
                    recommendations,
                    vee_explanations,
                    conversational_summary,
                    created_at
                ) VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, CURRENT_TIMESTAMP)
                RETURNING insight_id
            """, (
                insight.user_id,
                insight.snapshot_id,
                insight.insight_type,
                insight.severity,
                insight.title,
                insight.description,
                json.dumps(insight.metrics),
                json.dumps(insight.recommendations),
                json.dumps(insight.vee_explanations) if insight.vee_explanations else '{}',
                insight.conversational_summary or None
            ))
            
            insight_id = cur.fetchone()[0]
            self.postgres.connection.commit()
            
            logger.info(f"✅ Insight saved - ID: {insight_id}, Type: {insight.insight_type}")
            if insight.conversational_summary:
                logger.info(f"   Conversational: {insight.conversational_summary[:80]}...")
            return insight_id
    
    def get_user_insights(
        self,
        user_id: str,
        limit: int = 10,
        insight_type: Optional[str] = None
    ) -> List[GuardianInsight]:
        """
        Retrieve insights for user
        
        Args:
            user_id: User identifier
            limit: Maximum number of insights to return
            insight_type: Filter by insight type (optional)
        
        Returns:
            List of GuardianInsight objects
        """
        with self.postgres.connection.cursor() as cur:
            if insight_type:
                cur.execute("""
                    SELECT * FROM guardian_insights
                    WHERE user_id = %s AND insight_type = %s
                    ORDER BY created_at DESC
                    LIMIT %s
                """, (user_id, insight_type, limit))
            else:
                cur.execute("""
                    SELECT * FROM guardian_insights
                    WHERE user_id = %s
                    ORDER BY created_at DESC
                    LIMIT %s
                """, (user_id, limit))
            
            rows = cur.fetchall()
            columns = [desc[0] for desc in cur.description]
            
            insights = []
            for row in rows:
                data = dict(zip(columns, row))
                insights.append(GuardianInsight(
                    insight_id=data["insight_id"],
                    user_id=data["user_id"],
                    snapshot_id=data["snapshot_id"],
                    insight_type=data["insight_type"],
                    severity=data["severity"],
                    title=data["title"],
                    description=data["description"],
                    metrics=data["metrics"],
                    recommendations=data["recommendations"],
                    created_at=data["created_at"]
                ))
            
            return insights


